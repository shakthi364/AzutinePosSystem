    private void loadEmployeeType(){
        try {
            ResultSet resultset = MySQL.executeSearch("SELECT * FROM `employee_type`");
            
            Vector<String> vecter = new Vector<>();
            vecter.add("Select Employee");
            
            while(resultset.next()){
                vecter.add(resultset.getString("name"));
                employeeType.put(resultset.getString("name"), resultset.getString("id"));
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(vecter);
            jComboBox2.setModel(model);
        } catch (Exception e) {
        }
    }
    
